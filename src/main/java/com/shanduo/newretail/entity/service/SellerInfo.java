package com.shanduo.newretail.entity.service;


public class SellerInfo {
	 	private String id;

	    private String sellerName;

	    private String sellerPicture;
	    
	    private Integer sellerType;

	    private Boolean businessSign;
	    
	    private String distance;
	    
	    private Integer distribution;

		public String getId() {
			return id;
		}

		public void setId(String id) {
			this.id = id;
		}

		public String getSellerName() {
			return sellerName;
		}

		public void setSellerName(String sellerName) {
			this.sellerName = sellerName;
		}

		public String getSellerPicture() {
			return sellerPicture;
		}

		public void setSellerPicture(String sellerPicture) {
			this.sellerPicture = sellerPicture;
		}

		public Integer getSellerType() {
			return sellerType;
		}

		public void setSellerType(Integer sellerType) {
			this.sellerType = sellerType;
		}

		public Boolean getBusinessSign() {
			return businessSign;
		}

		public void setBusinessSign(Boolean businessSign) {
			this.businessSign = businessSign;
		}

		public String getDistance() {
			return distance;
		}

		public void setDistance(String distance) {
			this.distance = distance;
		}

		public Integer getDistribution() {
			return distribution;
		}

		public void setDistribution(Integer distribution) {
			this.distribution = distribution;
		}
		
	    

}
